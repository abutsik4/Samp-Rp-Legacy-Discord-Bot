/* ─── find the admin announcements channel ─── */
function findAdminAnnouncementsChannel(guild) {
  return guild.channels.cache.find(
    ch => ch.name === '📢│объявления-админ' && ch.parent && ch.parent.name.includes('Администрация')
  ) || null;
}


/* ─── post guide to admin announcements channel ─── */
async function postAdminGuide(guild) {
  const channel = findAdminAnnouncementsChannel(guild);
  if (!channel) throw new Error('Канал 📢│объявления-админ не найден. Сначала разверните структуру.');

  const embeds = buildGuideEmbeds();

  // Discord limit: 6000 chars total per message across all embeds.
  // Send embeds in batches that fit under the limit.
  let batch = [];
  let batchSize = 0;
  let sent = 0;

  for (const embed of embeds) {
    const embedJson = embed.toJSON();
    const charCount = (embedJson.title || '').length
      + (embedJson.description || '').length
      + (embedJson.footer?.text || '').length
      + 50; // overhead for fields, author, etc.

    if (batch.length > 0 && batchSize + charCount > 5800) {
      await channel.send({ embeds: batch });
      sent += batch.length;
      batch = [];
      batchSize = 0;
    }
    batch.push(embed);
    batchSize += charCount;
  }

  if (batch.length > 0) {
    await channel.send({ embeds: batch });
    sent += batch.length;
  }

  console.log('[GUIDE] Admin guide posted in #📢│объявления-админ (' + sent + ' embeds)');
  return { channelId: channel.id, channelName: channel.name, embedCount: sent };
}

